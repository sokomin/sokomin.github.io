// 名前から探す。idはわからんかった
function calcReqText(jn, ss, skill_name, jobid) {
    var res = "";
    var re = / /g;
    skill_name = skill_name.replace(re, "");
    if (!jn || !ss) {
        return res;
    }
    for (var i = 0; i < ss.length; i++) {
        var data = ss[i];
        if (data[0] == skill_name) {
            res = searchReqSkillTxt(ss, i);
            if (skill_name == "ヒーリング" && jobid == 5) {
                continue;
            } else {
                break;
            }
        }
    }
    if (!res) {
        res = "なし";
    }
    return res;
};

function searchReqSkillTxt(ss, tid) {
    var res = "";
    if (!ss[tid]) {
        return res;
    }
    var t0 = ss[tid][2];
    var t1 = ss[tid][4];
    var t2 = ss[tid][6];
    var t3 = ss[tid][8];
    var t4 = ss[tid][10];
    if (t0 > 0) {
        res += ss[t0][0] + '[' + ss[tid][3] + ']  ';
    }
    if (t1 > 0) {
        res += ss[t1][0] + '[' + ss[tid][5] + ']  ';
    }
    if (t2 > 0) {
        res += ss[t2][0] + '[' + ss[tid][7] + ']  ';
    }
    if (t3 > 0) {
        res += ss[t3][0] + '[' + ss[tid][9] + ']  ';
    }
    if (t4 > 0) {
        res += ss[t4][0] + '[' + ss[tid][11] + ']  ';
    }
    return res;
}